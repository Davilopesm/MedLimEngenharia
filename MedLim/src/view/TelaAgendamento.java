    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        long CPFPaciente = Long.parseLong(jTextField2.getText());
        String nomeMedico = jTextField1.getText();
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy", Locale.getDefault());//pegar data do jchooser
        String data = sdf.format(jDateChooser1.getDate());
        String horario = jComboBox1.getSelectedItem().toString();
        
        if(Control.pacienteExiste(CPFPaciente) == true){
            if(Control.medicoExiste(nomeMedico) == true){
                if(Control.horarioJaUsado(nomeMedico, data, horario) == false){
                    if(Control.agendarConsulta(CPFPaciente, nomeMedico, data, horario) == true){
                        JOptionPane optionPane = new JOptionPane("Consulta Agendada com sucesso");
                        JDialog dialog = optionPane.createDialog("Dados Salvos");
                        dialog.setAlwaysOnTop(this.isAlwaysOnTopSupported());
                        dialog.setVisible(true);     
                    }
                    else{//se n conseguiu marcar consulta
                        JOptionPane optionPane = new JOptionPane("N達o foi possivel marcar no momento, tente mais tarde");
                        JDialog dialog = optionPane.createDialog("Erro");
                        dialog.setAlwaysOnTop(this.isAlwaysOnTopSupported());
                        dialog.setVisible(true);   
                    }
                }
                else{//se horario esta reservado
                    String[] teste = Control.horariosVagos(nomeMedico, data);
                    JOptionPane optionPane = new JOptionPane("Horario ja esta reservado!\nHorarios de " +nomeMedico+ " ja reservados em " +data+" s達o:\n" +Arrays.toString(teste));
                    JDialog dialog = optionPane.createDialog("Horario Reservado");
                    dialog.setAlwaysOnTop(this.isAlwaysOnTopSupported());
                    dialog.setVisible(true);                    
                }
            }
            else{
                JOptionPane optionPane = new JOptionPane("Medico n達o encontrado");
                JDialog dialog = optionPane.createDialog("Erro");
                dialog.setAlwaysOnTop(this.isAlwaysOnTopSupported());
                dialog.setVisible(true);     
            }
        }
        else{
            JOptionPane optionPane = new JOptionPane("Paciente n達o esta cadastrado");
            JDialog dialog = optionPane.createDialog("Cadastre");
            dialog.setAlwaysOnTop(this.isAlwaysOnTopSupported());
            dialog.setVisible(true);   
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        long CPFPaciente = Long.parseLong(jTextField3.getText());
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy", Locale.getDefault());//pegar data do jchooser
        String data = sdf.format(jDateChooser2.getDate());
        String horario = jComboBox2.getSelectedItem().toString();
        
        if(Control.deletarConsulta(CPFPaciente, data, horario) == true){
            JOptionPane optionPane = new JOptionPane("Consulta desmarcada com sucesso!");
            JDialog dialog = optionPane.createDialog("Dados Salvos");
            dialog.setAlwaysOnTop(this.isAlwaysOnTopSupported());
            dialog.setVisible(true);   
        }
        else{
            JOptionPane optionPane = new JOptionPane("Erro ao desmarcar consulta, tente novamente");
            JDialog dialog = optionPane.createDialog("Erro");
            dialog.setAlwaysOnTop(this.isAlwaysOnTopSupported());
            dialog.setVisible(true); 
        }
    }//GEN-LAST:event_jButton2ActionPerformed

