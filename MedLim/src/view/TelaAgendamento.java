    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        long CPFPaciente = Long.parseLong(jTextField2.getText());
        String nomeMedico = jTextField1.getText();
        SimpleDateFormat sdf = new SimpleDateFormat("dd-MM-yyyy", Locale.getDefault());//pegar data do jchooser
        String data = sdf.format(jDateChooser1.getDate());
        String horario = jComboBox1.getSelectedItem().toString();
        
        if(Control.pacienteExiste(CPFPaciente)){
            if(Control.medicoExiste(nomeMedico)){
                if(!Control.horarioJaUsado(nomeMedico, data, horario)){
                    if(Control.agendarConsulta(CPFPaciente, nomeMedico, data, horario)){
                        JOptionPane optionPane = new JOptionPane("Consulta Agendada com sucesso");
                        JDialog dialog = optionPane.createDialog("Dados Salvos");
                        dialog.setAlwaysOnTop(this.isAlwaysOnTopSupported());
                        dialog.setVisible(true);     
                    }
                    else{//se n conseguiu marcar consulta
                        JOptionPane optionPane = new JOptionPane("Não foi possivel marcar no momento, tente mais tarde");
                        JDialog dialog = optionPane.createDialog("Erro");
                        dialog.setAlwaysOnTop(this.isAlwaysOnTopSupported());
                        dialog.setVisible(true);   
                    }
                }
                else{//se horario esta reservado
                    String[] teste = Control.horariosVagos(nomeMedico, data);
                    JOptionPane optionPane = new JOptionPane("Horario ja esta reservado!\nHorarios de " +nomeMedico+ " ja reservados em " +data+" são:\n" +Arrays.toString(teste));
                    JDialog dialog = optionPane.createDialog("Horario Reservado");
                    dialog.setAlwaysOnTop(this.isAlwaysOnTopSupported());
                    dialog.setVisible(true);                    
                }
            }
            else{
                JOptionPane optionPane = new JOptionPane("Medico não encontrado");
                JDialog dialog = optionPane.createDialog("Erro");
                dialog.setAlwaysOnTop(this.isAlwaysOnTopSupported());
                dialog.setVisible(true);     
            }
        }
        else{
            JOptionPane optionPane = new JOptionPane("Paciente não esta cadastrado");
            JDialog dialog = optionPane.createDialog("Cadastre");
            dialog.setAlwaysOnTop(this.isAlwaysOnTopSupported());
            dialog.setVisible(true);   
        }
    }//GEN-LAST:event_jButton1ActionPerformed

